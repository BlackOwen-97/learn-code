function Nintegrate1(f::Function,a,b,eps)
    ########################################################################
    ### Nintegrate1: 计算一重积分; 
    ### 输入包括：
    ### f 被积函数
    ### a 积分下限
    ### b 积分上限
    ### eps 数值积分的精度要求
    #########################################################################

    # 积分的上下限相等，积分值为零
    if a==b
        return 0
    end
    c0=0.005/(b-a);
    
    bb=b;
    aa=a;
    NI1=0;
    while true
        c1=0.5*(bb+aa);
        c2=0.5*(bb-aa);
        # 8点高斯积分的计算
        x8,w8=Gausspoints(8,aa,bb);
        s8=sum(f.(x8).*w8);
        # 16点高斯积分的计算
        x16,w16=Gausspoints(16,aa,bb);
        s16=sum(f.(x16).*w16);
        if abs(s16-s8)<=eps*(1.0+abs(s16))
            NI1=NI1+s16;
            if bb==b
                return NI1
            end
            aa=bb;
            bb=b;
        else
            bb=c1;
        end
        if 1.0+abs(c0*c2)==1.0
            println("Nintegrate1的积分精度要求太高")
            return 0
        end 
    end
    
end

########################################################################
### Nintegrate2: 计算二重积分; 
### 输入包括：
### f 被积函数
### xl x轴的积分下限
### xu x轴的积分上限
### yl y轴的积分下限
### yu y轴的积分上限
### eps 数值积分的精度要求
#########################################################################
function Nintegral20(f::Function,xl,xu,yl,yu,Nnode)
    x,wx=Gausspoints(Nnode,xl,xu);
    y,wy=Gausspoints(Nnode,yl,yu);
    Res=0;
    for i=1:Nnode
        for j=1:Nnode
            Res=Res+wx[i]*wy[j]*f(x[i],y[j]);
        end
    end
    return Res
end
function QLQH(f::Function,xl,xu,yl,yu)
    QL=Nintegral20(f,xl,xu,yl,yu,4); # 4点高斯积分
    QH=Nintegral20(f,xl,xu,yl,yu,8); # 8点高斯积分
    return QL,QH
end
